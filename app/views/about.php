	<div class="row-fluid"><h1 style="margin-bottom: 10px;">Combineren</h1></div>
	<div class="row-fluid">
		<p>CultuurApp centreert zich om monumenten en evenementen in Amsterdam (en voor monumenten zelfs Nederland). De beschikbare gegevens van deze monumenten en evenementen zijn verschillend per databron. Toch moet een monument of evenement die op verschillende databronnen te vinden is, slechts eenmaal te vinden zijn op CultuurApp. Locatiegewijs kunnen alle databronnen worden gekoppeld aan een monument, immers, de locatie van een monument is gegeven en vast (op de molen in Delft na, die staat op rails). Hierdoor is de kaartweergave een centrale feature van CultuurApp. Deze is overzichtelijk en voor iedereen begrijpbaar. Door foto’s uit de omgeving aan een monument te koppelen, evenals restaurants in de buurt, cafés in de buurt, weersverwachting voor de omgeving, kaartgegevens, fourSquare locaties etc. krijgt de gebruiker op een overzichtelijke wijze gegevens gekoppeld van negen verschillende databronnen. Handmatig al deze bronnen koppelen bij een gegeven monument zou een dagtaak zijn, CultuurApp koppelt deze (vaak erg variabele en dynamische) gegevens volledig geautomatiseerd en haalt data van de verschillende databronnen live op voor accurate gegevens. CultuurApp is hiermee 9 in 1, ons doel is dan ook om de enige databron te zijn die een toerist nodig heeft.</p>
